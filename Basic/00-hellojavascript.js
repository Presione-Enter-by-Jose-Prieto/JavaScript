/* 
Este es un simple programa en JavaScript 
que imprime "Hello, JavaScript!" en la consola.
*/

console.log("Hello, JavaScript!")
console.log('Hello, JavaScript!')
console.log(`Hello, JavaScript!`)

/*
Esto es un número dentro de una cadena de texto
el cual no se puede usar en operaciones matemáticas.
*/

console.log("5")
console.log("5" + "5")

/*
Esto es un número que se puede usar en 
operaciones matemáticas.
*/

console.log(5)
console.log(5 + 5)
console.log(5 + 2 + 3)
console.log(5 - 5)
console.log(5 * 5)
console.log(5 / 5)